# Loops - Do something over and over until a certain criteria is met

# While loop
# Print numbers from 1 to 10
print("- While Loop -")
count = 1
while(count <= 10):
    print(count)

    # Increase value of count
    count += 1  # Can also be written as: count = count + 1

# Create a blank space
print()

# For Loop
## Print numbers from 1 to 10
print("- For Loop -")
for x in range(1, 11):
    print(x)

